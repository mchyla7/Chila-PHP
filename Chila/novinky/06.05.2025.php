        <h1>Lewis Hamilton: "F1 The Movie bude nejautentičtějším závodním filmem všech dob"</h1>
        <div class="clanek-meta">
            <span class="datum">6. května 2025</span>
            <span class="autor">Redakce F1-Chila</span>
        </div>
    </div>
    
    <div class="clanek-obsah">
        <p><strong>Očekávání roste před uvedením filmu F1 The Movie – 25. června mezinárodně a 27. června v Severní Americe – a jedním z těch, kdo je obzvláště nadšený, aby fanoušci film viděli, je Lewis Hamilton.</strong></p>

        <p>Poté, co působil jako producent tohoto filmu, sedminásobný mistr světa otevřeně mluví o "neuvěřitelném procesu učení", svých zkušenostech s prací s Bradem Pittem a o tom, proč věří, že to bude "nejautentičtější závodní film, který kdy byl natočen"...</p>

        <h3>🎬 O roli producenta filmu F1</h3>
        
        <p><em><strong>Povězte mi o vstupu do role producenta filmu F1 The Movie a co pro vás znamená přinést Formuli 1 na velké plátno způsobem, který nikdy předtím nebyl vidět?</strong></em></p>

        <p>"Zapojení do tohoto projektu jako producent bylo pro mě neuvěřitelným procesem učení. Viděl jsem, co vše se do natáčení celovečerního filmu vkládá, a bylo to hodně učení i na druhé straně, protože se každý musel seznámit s Formulí 1.</p>

        <p>Vždy jsem miloval film a vyprávění příběhů a tento sport miluji od svých pěti let, takže když jsem dostal příležitost mluvit s Joe Kosinskim a Jerrym Bruckheimerem o projektu, cítil jsem, jako by se spojily dva světy, které mě nadchnou.</p>

        <p>Pamatuju si, že na začátku projektu jsem se mohl setkat s Bradem a projít si scénář, účastnit se schůzek s týmy z Apple a Formule 1 a být součástí některých raných procesů castingu. Od těch prvních rozhovorů až po sledování toho všeho, jak se to skládá dohromady – byla to prostě super cool zkušenost a nemůžu se dočkat, až to všichni uvidí!"</p>

        <h3>🏎️ Proč nejautentičtější závodní film?</h3>
        
        <p><em><strong>Proč bude tento film nejautentičtějším závodním filmem všech dob?</strong></em></p>

        <p>"Přístup, který jsme měli k Formuli 1, je naprosto nevídaný. Skutečně jsme natáčeli během závodních víkendů s 11. týmem filmu – APXGP, a tuto autenticitu v filmu určitě uvidíte.</p>

        <p>Záběry natočené během závodního kalendáře budou smíchány do filmu a rozšířeny digitálními technikami, aby se do skutečných závodních záběrů vložily filmové týmové vozy, takže to bude pro fanoušky sportu vypadat opravdu realisticky.</p>

        <p>Ale skutečně uvítali i mé pozorování menších detailů, jako jsou zvuky, které by vůz vydával, když je na určité pozici na trati, nebo způsob, jakým by se mohla stát nehoda. To je to, co Joe [Kosinski, režisér] a Jerry [Bruckheimer, producent] chtěli od začátku – vytvořit nejautentičtější závodní film, který kdy byl natočen."</p>

        <h3>🎥 Práce s legendárním duem</h3>
        
        <p><em><strong>Zmiňujete filmaře Joe Kosinského a Jerryho Bruckheimera, kteří společně dosáhli nových kinematografických výšin s Top Gun: Maverick. Jak na tento úspěch film F1 The Movie naváže?</strong></em></p>

        <p>"Vyrůstal jsem s tím, že jsem viděl Jerryho jméno na plátně v kině, a je to taková legenda, takže být konečně v místnosti a pracovat s ním byla taková čest.</p>

        <p>Stále je tak nadšený, i přes všechna ocenění a veškerý úspěch, který měl, je prostě tak vášnivý pro to, co dělá, a to bylo tak inspirující vidět. A byl jsem opravdu, opravdu ohromený Joe a tím, jak pracuje, jeho laserové zaměření, jeho nápady, jeho oko pro to, jak věci zachytit."</p>

        <h3>📹 Technologické inovace</h3>
        
        <p><em><strong>Jako producent filmu, jaké inovace jste zaznamenal, které by učinily tento film nepodobným jakémukoli jinému závodnímu filmu v historii?</strong></em></p>

        <p>"Vývoj technologie. Umístění kamery. Něco z toho, co použili se stíhačkami v Top Gun: Maverick, Joe přinesl část této technologie do tohoto filmu, a jeho tým a náš kameraman Claudio Miranda, pracující s Apple, to posunuli na jinou úroveň.</p>

        <p>Vyvinuli tento kamerový systém a je to tak jiné, mnohem pokročilejší než to, co sledujete, když se díváte na závod během víkendu. Záběry jsou mnohem syrovější, takže když se díváte na film, opravdu máte pocit, jako byste byli v autě a jeli velmi vysokými rychlostmi."</p>

        <h3>🤝 Spolupráce s Bradem Pittem</h3>
        
        <p><em><strong>Povězte mi o práci s Bradem Pittem – je také spoluproducentem a zároveň hraje ve filmu.</strong></em></p>

        <p>"Nikdy jsem si nemyslel, že budu moci pracovat s Bradem Pittem. Jsem velkým fanouškem jeho filmů a myslím, že jsem se s ním poprvé setkal v Jerryho kanceláři přes Zoom, což bylo dost šílené, ale pak jsme se dostali na trať a zjistil jsem, jak moc se zajímá o motorsport, zejména o motorky, a viděl přirozenou schopnost, kterou měl za volantem. Bylo to opravdu cool.</p>

        <p>Pamatuju si, jak jsme oba stáli na okruhu Silverstone, u zatáčky Copse, a já mu ukazoval věci, na které se dívám, když do této zatáčky vjíždím, a mluvili jsme o tom, že jezdci jsou v konečném důsledku elitní sportovci a je toho tolik, co do závodění vstupuje a co nevidíte; G-síly a všechno, čím naše těla procházejí, rigorózní trénink, který musíme absolvovat, abychom byli schopni tyto závody jet.</p>

        <p>Opravdu se zajímal o tuto úroveň pohledu, aby mohl přinést co nejvíce autenticity do své role."</p>

        <h3>🏁 Autenticita díky skutečnému řízení</h3>
        
        <p><em><strong>Část autenticity filmu spočívá v tom, že Brad a Damson skutečně řídí tyto vozy na skutečných závodech. Proč bylo důležité pro vás jako filmaře?</strong></em></p>

        <p>"Myslím, že to, že Brad a Damson řídili, bylo nedílnou součástí autenticity příběhu, protože během své kariéry jsem lidem říkal, jak obtížné to v závodě je, a oni říkali: 'Kolik kilos jsi zhubl?' nebo 'Jaké máš modřiny po závodě?' nebo 'Cože? Nemáte v autě klimatizaci?'</p>

        <p>Je to super náročné. A ano, během závodu můžeme zhubnout až 4,5 kilogramu. Takže myslím, že pro tyto dva herce bylo důležité projít si proces mentálních výzev, kterými procházíme jako závodní jezdci – rychlost, kterou zpracováváte informace, vliv na tělo, jak silné jsou brzdy, G-síly...</p>

        <p>Myslím, že to pro ně bylo opravdu důležité, protože to nemůžete předstírat. Pokud jste to nezažili, nemůžete si to představit. Řídením těchto vozů – a oba odvedli skvělou práci – myslím, že oba získali skutečné ocenění toho, co být závodním jezdcem opravdu znamená, a to je to, co uvidíte ve filmu."</p>

        <h3>🎭 Očekávání premiéry</h3>
        
        <p><em><strong>Co pro vás znamená vědět, že diváci konečně uvidí tento film, který jste produkoval, na velkém plátně?</strong></em></p>

        <p>"Jít od námětu k vidění celého scénáře, práce na scénáři, pak procházet vývojem kamer, celým tím procesem až k tomu, že to teď vidím na velkém plátně... je to úžasné. Nemůžu se dočkat reakcí lidí a toho, jak je to rozezní.</p>

        <p>Je tam tolik autenticity a opravdu cítíte rychlost a soutěžení mezi jezdci a to, jak moc chtějí vyhrát – cítí se to, jako by bylo všechno v sázce.</p>

        <p>Je nás jen 20 na světě, kteří jezdíme Formuli 1, a tohle přinese tuto zkušenost lidem a dá všem šanci pocítit, jaké to je být za volantem jednoho z našich vozů. Upřímně se nemůžu dočkat."</p>
