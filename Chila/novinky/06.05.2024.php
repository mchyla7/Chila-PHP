<div class="clanek-container">
    <div class="clanek-header">
        <h1>FIA odhalila "agilní, konkurenceschopné, bezpečnější a udržitelnější" technické předpisy pro rok 2026</h1>
        <div class="clanek-meta">
            <span class="datum">28. června 2024</span>
            <span class="autor">Redakce F1-Chila</span>
        </div>
    </div>
    
    <div class="clanek-obsah">
        <p><strong>Řídící orgán Formule 1 FIA odhalil sadu předpisů "agilní, konkurenceschopné, bezpečnější a udržitelnější", které budou definovat sport od roku 2026 a dále.</strong></p>

        <p>Revidované aerodynamické předpisy budou fungovat společně s novými pravidly pro pohonné jednotky, která zahrnují zvýšený výkon baterie a použití 100% udržitelných paliv.</p>

        <h3>🚗 Co přinese Formule 1 od roku 2026</h3>
        
        <div class="zmeny-box">
            <ul class="zmeny-seznam">
                <li><strong>Agilnější vozy</strong> - o 30 kg lehčí, lépe schopné bojovat na trati</li>
                <li><strong>Přepracovaná pohonná jednotka</strong> - zvýšený výkon baterie a rovnoměrné rozdělení mezi spalovací motor a elektrický pohon plus použití 100% udržitelných paliv</li>
                <li><strong>Aktivní aerodynamika</strong> - pohyblivá přední a zadní křídla pro umožnění těsnějšího závodění</li>
                <li><strong>Zvýšené příležitosti k předjíždění</strong> - nový systém poskytuje jezdcům krátký nárůst dodatečného výkonu baterie, když jsou do jedné sekundy za vozem před sebou</li>
                <li><strong>Zlepšená bezpečnost</strong> - silnější struktury a ještě přísnější testy</li>
                <li><strong>Rekordní počet výrobců</strong> - závazek od rekordního počtu šesti výrobců pohonných jednotek</li>
            </ul>
        </div>

        <p>Revidované technické předpisy byly stanoveny FIA, což připravilo cestu k jejich odhalení v Montrealu před kanadskou Velkou cenou.</p>

        <p>Předpisy pro rok 2026 mají být nyní ratifikovány Světovou radou motoristického sportu 28. června.</p>

        <h3>🔧 Vize "agilního vozu"</h3>
        
        <p><em>Technický ředitel FIA pro monoposty Nikolas Tombazis řekl:</em></p>
        
        <blockquote>
            "S touto sadou předpisů se FIA snažila vyvinout novou generaci vozů, která jsou plně v souladu s DNA Formule 1 – vozy, které jsou lehké, extrémně rychlé a agilní, ale které také zůstávají na špičce technologie. Abychom toho dosáhli, pracovali jsme směrem k tomu, co jsme nazvali konceptem 'obratného vozu'."
        </blockquote>

        <h3>⚡ Redesign pohonných jednotek</h3>
        
        <p>"V centru této vize je přepracovaná pohonná jednotka, která představuje rovnoměrnější rozdělení mezi výkon odvozený od spalovacího elementu a elektrický výkon. Na straně šasi jsme dokázali snížit velikost a hmotnost vozu o 30 kg, což má za následek mnohem dynamičtější vůz."</p>

        <p>"Kromě toho zavádíme dvě vzrušující nové funkce pro zlepšení závodění – aktivní aerodynamiku pro dosažení velmi nízkého odporu na rovinkách a systém Manual Override, který poskytne jezdcům výkon baterie na požádání, když jsou dostatečně blízko vozu před sebou."</p>

        <h3>📊 Konkrétní změny v číslech</h3>
        
        <div class="statistiky-zmeny">
            <div class="stat-item">
                <h4>Pohonné jednotky</h4>
                <p><strong>Hybridní výkon:</strong> Pokles z 550-560kW na 400kW</p>
                <p><strong>Elektrický výkon:</strong> Nárůst ze 120kW na 350kW (nárůst o téměř 300%)</p>
                <p><strong>Energie z brzdění:</strong> Zdvojnásobeno na 8,5 MJ na kolo</p>
            </div>
            
            <div class="stat-item">
                <h4>Rozměry a hmotnost</h4>
                <p><strong>Rozvor:</strong> Snížení o 200mm na 3400mm</p>
                <p><strong>Šířka:</strong> Snížení o 100mm na 1900mm</p>
                <p><strong>Hmotnost:</strong> Minimálně 768kg (o 30kg méně)</p>
                <p><strong>Přítlačná síla:</strong> Snížení o 30%</p>
                <p><strong>Odpor:</strong> Snížení o 55%</p>
            </div>
        </div>

        <h3>🏁 Nové aerodynamické inovace</h3>
        
        <p>FIA uvádí, že vozy z roku 2026 budou přepínatelné mezi dvěma konfiguracemi - buď pro minimalizaci spotřeby paliva, nebo pro maximalizaci výkonu v zatáčkách.</p>

        <p>Budou zavedeny zcela nové systémy aktivní aerodynamiky zahrnující pohyblivá přední a zadní křídla. To umožní větší rychlosti v zatáčkách s jedním úhlem křídla a nižší odpor s větší rychlostí na rovinkách při druhém úhlu.</p>

        <p>Tento nový systém může být použit v zónách podobných těm, které se používají pro současný DRS systém pro předjíždění.</p>

        <h3>🛡️ Pokračující zaměření na bezpečnost</h3>
        
        <p>FIA revidovala pravidla pro přední nárazovou strukturu a zavedla dvoustupňový design nosu pro zmírnění rizika odtržení při prvotních nárazech.</p>

        <p>Pravidla pro boční intrusi jsou také přísnější, zatímco ochrana kolem oblasti jezdce a palivové nádrže byla zlepšena.</p>

        <h3>🌱 Udržitelnost v centru pozornosti</h3>
        
        <p>Zatímco současné pohonné jednotky Formule 1 jsou již nejefektivnější na světě, v úsilí F1 a FIA dosáhnout uhlíkové neutrality do roku 2030 budou pohonné jednotky z roku 2026 jezdit na plně udržitelná paliva.</p>

        <p>V jednotkách z roku 2026 bude větší využití elektrického výkonu s posunem směrem k 50% elektrickému a 50% tepelnému rozdělení výkonu.</p>

        <h3>🏭 Rekordní počet výrobců</h3>
        
        <p>Nové předpisy již vedly k závazkům stávajících dodavatelů Ferrari, Mercedes a Renault - zatímco přilákaly Hondu k návratu a povzbudily příchod Audi a Ford, přičemž druhý jmenovaný vstupuje prostřednictvím partnerství s Red Bull Powertrains.</p>

        <h3>💭 Slovo prezidenta FIA</h3>
        
        <p><em>Prezident FIA Mohammed Ben Sulayem řekl:</em></p>
        
        <blockquote>
            "Dnes FIA definuje nesmírně vzrušující budoucnost pro vrchol motoristického sportu spuštěním komplexní nové sady předpisů pro mistrovství FIA Formule 1 roku 2026 a dále. Klíčovými rysy předpisů F1 pro rok 2026 jsou pokročilé technologie udržitelnosti a bezpečnosti. Naším cílem společně s Formulí 1 bylo vytvořit vůz, který by byl správný pro budoucnost elitní kategorie sportu. Věříme, že tohoto cíle jsme dosáhli."
        </blockquote>

        <div class="zaver-box">
            <h4>🔮 Budoucnost je zde</h4>
            <p>Předpisy pro rok 2026 představují revoluční změnu ve Formuli 1, která kombinuje špičkovou technologii s udržitelností a bezpečností. S lehčími, rychlejšími a ekologičtějšími vozy slibuje F1 ještě vzrušivější a odpovědnější budoucnost motoristického sportu.</p>
        </div>
    </div>
